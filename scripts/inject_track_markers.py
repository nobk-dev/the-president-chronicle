def strip_markers(s: str) -> str:
    """既存の ★/★★ マーカーをすべて除去。"""
    # 行頭 / ／ 直後の ★★ → 削除
    s = s.replace("★★", "")
    s = s.replace("★", "")
    return s


def inject_markers(text: str, mids: list, majs: list) -> str:
    """text を／で分割し、各項目について majs/mids の先頭一致をチェックして
    マーカーを差し込む。事前にすべての ★ を除去したクリーンな状態を仮定。"""
    parts = []
    buf = ""
    depth = 0
    for ch in text:
        if ch in "［[":
            depth += 1
        elif ch in "］]":
            depth = max(0, depth - 1)
        if ch == "／" and depth == 0:
            parts.append(buf)
            buf = ""
        else:
            buf += ch
    parts.append(buf)

    out = []
    for p in parts:
        s = p.strip()
        if not s:
            out.append(p)
            continue
        marked = False
        # 象徴を優先
        for prefix in majs:
            if s.startswith(prefix.strip()):
                left = p[: len(p) - len(p.lstrip())]
                right_idx = len(p.rstrip())
                out.append(f"{left}★★{p[len(left):right_idx]}{p[right_idx:]}")
                marked = True
                break
        if marked:
            continue
        for prefix in mids:
            if s.startswith(prefix.strip()):
                left = p[: len(p) - len(p.lstrip())]
                right_idx = len(p.rstrip())
                out.append(f"{left}★{p[len(left):right_idx]}{p[right_idx:]}")
                marked = True
                break
        if not marked:
            out.append(p)

    return "／".join(out)


