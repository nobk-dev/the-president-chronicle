            old = y[field]
            new = inject_markers(old, mids, majs)
            y[field] = new
            # 期待マッチ数を確認
            for prefix in majs:
                if f"★★{prefix}" in new or new.find(f"★★{prefix}") >= 0:
                    matched_count["maj"] += 1
                else:
                    # しらみつぶしでチェック
                    if f"★★{prefix}" not in new:
                        unmatched.append((year, field, "maj", prefix))
            for prefix in mids:
                if f"★{prefix}" in new and not new.startswith(f"★★{prefix}"):
                    matched_count["mid"] += 1

